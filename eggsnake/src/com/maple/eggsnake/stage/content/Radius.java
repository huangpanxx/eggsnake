package com.maple.eggsnake.stage.content;

public class Radius {
	
	private float radius;
	
	public Radius(float radius){
		this.radius = radius;
	}

	/**
	 * @return the radius
	 */
	public float getRadius() {
		return radius;
	}

	/**
	 * @param radius the radius to set
	 */
	public void setRadius(float radius) {
		this.radius = radius;
	}
	
}
